#[allow(dead_code)]
fn generate_alias(name: &str, target: &crate::ir::TypeRef, desc: &Option<String>) -> String {
    let mut lines = Vec::new();
    if let Some(d) = desc {
        lines.push(format!("/** {} */", d));
    }
    lines.push(format!("export type {} = {};", to_pascal(name), target.to_ts()));
    lines.join("\n")
}

fn generate_primitive_alias(
    name: &str,
    prim_type: &str,
    format: Option<&str>,
    desc: &Option<String>,
) -> String {
    let mut lines = Vec::new();
    if let Some(d) = desc {
        lines.push(format!("/** {} */", d));
    }
    let ts_type = match (prim_type, format) {
        ("string", Some("uuid")) => "string",
        ("string", _) => "string",
        ("integer", _) | ("number", _) => "number",
        ("boolean", _) => "boolean",
        _ => "unknown",
    };
    lines.push(format!("export type {} = {};", to_pascal(name), ts_type));
    lines.join("\n")
}

fn generate_string_enum(name: &str, values: &[String], desc: &Option<String>) -> String {
    let mut lines = Vec::new();
    if let Some(d) = desc {
        lines.push(format!("/** {} */", d));
    }
    // Generate TypeScript string literal union: type Foo = "a" | "b" | "c";
    let union_values: Vec<String> = values.iter().map(|v| format!("\"{}\"", v)).collect();
    let union_type = if union_values.is_empty() {
        "never".to_string()
    } else {
        union_values.join(" | ")
    };
    lines.push(format!("export type {} = {};", to_pascal(name), union_type));
    lines.join("\n")
}

/// Convert to PascalCase
fn to_pascal(s: &str) -> String {
    let mut result = String::new();
    let mut capitalize = true;
    for c in s.chars() {
        if c == '_' || c == '-' || c == '.' {  // Treat dots as word boundaries
            capitalize = true;
        } else if capitalize {
            result.push(c.to_ascii_uppercase());
            capitalize = false;
        } else {
            result.push(c);
        }
    }
    result
}

/// Convert to camelCase
fn to_camel(s: &str) -> String {
    let pascal = to_pascal(s);
    if pascal.is_empty() {
        return pascal;
    }
    let mut chars = pascal.chars();
    match chars.next() {
        Some(first) => first.to_ascii_lowercase().to_string() + chars.as_str(),
        None => String::new(),
    }
}
